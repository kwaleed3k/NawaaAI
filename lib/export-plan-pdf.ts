import { jsPDF } from "jspdf";
import html2canvas from "html2canvas";

export type PlanDay = {
  dayIndex: number;
  dayEn: string;
  dayAr: string;
  date: string;
  platform: string;
  contentType: string;
  topic: string;
  topicAr?: string;
  caption: string;
  captionAr?: string;
  hashtags: string[];
  postingTime: string;
  postingTimeReason?: string;
  contentTips?: string;
  imagePromptHint?: string;
};

export type ContentPlan = {
  weekTheme: string;
  weekThemeAr: string;
  days: PlanDay[];
  weeklyStrategy?: string;
  expectedEngagement?: string;
};

export type Company = {
  id: string;
  name: string;
  name_ar?: string | null;
  brand_colors?: string[] | null;
};

const C = {
  green: "#23ab7e",
  greenDark: "#1a8a64",
  purple: "#8054b8",
  pink: "#e67af3",
  teal: "#2dd4a0",
  lavender: "#c4a8e8",
  mint: "#a6ffea",
  dark: "#1a1d2e",
  gray: "#2d3142",
  muted: "#8f96a3",
  light: "#f4f6f8",
  border: "#e8eaef",
  white: "#ffffff",
};

const DAY_ACCENTS = [C.green, C.purple, C.pink, C.teal, C.lavender, "#f5c6fa", C.mint];

function dayLabel(d: PlanDay, lang: "en" | "ar") { return lang === "ar" ? d.dayAr : d.dayEn; }
function topicLabel(d: PlanDay, lang: "en" | "ar") { return lang === "ar" && d.topicAr ? d.topicAr : d.topic; }
function captionLabel(d: PlanDay, lang: "en" | "ar") { return lang === "ar" && d.captionAr ? d.captionAr : d.caption; }
function companyLabel(company: Company, lang: "en" | "ar") { return lang === "ar" && company.name_ar ? company.name_ar : company.name; }
function themeLabel(plan: ContentPlan, lang: "en" | "ar") { return lang === "ar" && plan.weekThemeAr ? plan.weekThemeAr : plan.weekTheme; }

// Font family for Arabic — Cairo first (best html2canvas compatibility), then system Arabic fonts
const AR_FONT = "'Cairo', 'Tahoma', 'Arial', sans-serif";
const EN_FONT = "'Outfit', 'Plus Jakarta Sans', 'Segoe UI', sans-serif";

function baseStyle(lang: "en" | "ar") {
  const dir = lang === "ar" ? "rtl" : "ltr";
  const fontFamily = lang === "ar" ? AR_FONT : EN_FONT;
  return `direction: ${dir}; font-family: ${fontFamily}; text-align: ${lang === "ar" ? "right" : "left"};`;
}

// Wrap text that might be Arabic with proper attributes
function t(text: string, lang: "en" | "ar") {
  if (lang !== "ar") return text;
  return `<span dir="rtl" lang="ar" style="font-family: ${AR_FONT}; direction: rtl;">${text}</span>`;
}

