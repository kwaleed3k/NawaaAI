/* ═══════════════════════════════════
   EXPORT FUNCTION
   ═══════════════════════════════════ */
export async function exportPlanToPDF(
  plan: ContentPlan,
  company: Company,
  outputLanguage: "en" | "ar" = "ar"
): Promise<void> {
  const pdf = new jsPDF({ orientation: "landscape", unit: "mm", format: "a4" });
  const days = plan.days ?? [];
  if (days.length === 0) throw new Error("No days in plan");

  // Preload all fonts
  await document.fonts.ready;
  try {
    await Promise.all([
      document.fonts.load("400 16px 'Cairo'"),
      document.fonts.load("700 16px 'Cairo'"),
      document.fonts.load("900 16px 'Cairo'"),
      document.fonts.load("400 16px 'IBM Plex Sans Arabic'"),
      document.fonts.load("700 16px 'IBM Plex Sans Arabic'"),
      document.fonts.load("400 16px 'Outfit'"),
      document.fonts.load("700 16px 'Outfit'"),
    ]);
  } catch { /* continue */ }

  // Show loading overlay so user doesn't see render divs
  const overlay = document.createElement("div");
  overlay.style.cssText = `position: fixed; inset: 0; z-index: 99999; background: rgba(255,255,255,0.92); backdrop-filter: blur(8px); display: flex; align-items: center; justify-content: center;`;
  overlay.innerHTML = `<div style="text-align: center; font-family: 'Cairo','Outfit',sans-serif;">
    <div style="width: 64px; height: 64px; margin: 0 auto 16px; border-radius: 16px; background: linear-gradient(135deg, #23ab7e, #8054b8); display: flex; align-items: center; justify-content: center; color: white; font-size: 28px; font-weight: 900; animation: spin 1.5s linear infinite;">N</div>
    <p style="font-size: 18px; font-weight: 700; color: #2d3142; margin: 0 0 6px;">${outputLanguage === "ar" ? "جارٍ تصدير PDF..." : "Exporting PDF..."}</p>
    <p style="font-size: 14px; color: #8f96a3; margin: 0;">${outputLanguage === "ar" ? "لحظة واحدة" : "Just a moment"}</p>
    <style>@keyframes spin { from { transform: rotate(0deg); } to { transform: rotate(360deg); } }</style>
  </div>`;
  document.body.appendChild(overlay);

  async function renderPage(html: string) {
    const wrapper = document.createElement("div");
    // Render VISIBLE in the document flow so browser fully shapes Arabic text
    // Behind the loading overlay (z-index: 99998 < overlay's 99999)
    wrapper.style.cssText = `position: fixed; top: 0; left: 0; z-index: 99998; pointer-events: none; opacity: 0.01;`;
    if (outputLanguage === "ar") {
      wrapper.setAttribute("dir", "rtl");
      wrapper.setAttribute("lang", "ar");
    }
    wrapper.innerHTML = html;
    document.body.appendChild(wrapper);

    // Wait for browser to fully layout, shape glyphs, and paint
    await new Promise(r => setTimeout(r, 500));
    // Force a layout recalculation
    const target = wrapper.firstElementChild as HTMLElement;
    void target.offsetHeight;
    await new Promise(r => setTimeout(r, 200));

    try {
      const canvas = await html2canvas(target, {
        scale: 1.5,
        useCORS: true,
        backgroundColor: null,
        logging: false,
        allowTaint: true,
        onclone: (clonedDoc) => {
          // Ensure cloned document also has proper Arabic attributes
          const clonedEl = clonedDoc.querySelector("[dir]") as HTMLElement;
          if (clonedEl && outputLanguage === "ar") {
            clonedEl.style.direction = "rtl";
            clonedEl.lang = "ar";
          }
        },
      });
      const imgData = canvas.toDataURL("image/jpeg", 0.88);
      const pdfW = pdf.internal.pageSize.getWidth();
      const pdfH = pdf.internal.pageSize.getHeight();
      const ratio = Math.min(pdfW / canvas.width, pdfH / canvas.height) * 0.97;
      const w = canvas.width * ratio;
      const h = canvas.height * ratio;
      pdf.addImage(imgData, "JPEG", (pdfW - w) / 2, (pdfH - h) / 2, w, h);
    } finally {
      document.body.removeChild(wrapper);
    }
  }

  // Page 1: Cover
  await renderPage(renderCoverPage(plan, company, outputLanguage));

  // Pages 2-8: Days
  for (let i = 0; i < days.length; i++) {
    pdf.addPage();
    await renderPage(renderDayPage(days[i], outputLanguage, company, plan, i));
  }

  // Final page: Conclusion
  pdf.addPage();
  await renderPage(renderConclusionPage(plan, company, outputLanguage));

  // Remove loading overlay
  try { document.body.removeChild(overlay); } catch { /* already removed */ }

  const filename = `NawaaAI-${(company.name || "Plan").replace(/\s+/g, "-")}-Week-${days[0]?.date ?? "plan"}.pdf`;
  pdf.save(filename);
}
